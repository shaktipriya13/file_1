s1=set()
s1.update("shakti")
print(s1)
# The update() method takes an iterable as an argument and adds its elements to the set. In this case, the string "kl" is an iterable of characters, so each character in the string is added as an individual element to the set s1.

# As a result, the set s1 will contain the elements of shakti. The order of elements in a set is not guaranteed,
